package com.One2Many;

public class Songs {
private Integer sid;
private String sname;
private Integer fid;
public Integer getSid() {
	return sid;
}
public void setSid(Integer sid) {
	this.sid = sid;
}
public String getSname() {
	return sname;
}
public void setSname(String sname) {
	this.sname = sname;
}
public Integer getFid() {
	return fid;
}
public void setFid(Integer fid) {
	this.fid = fid;
}


}
